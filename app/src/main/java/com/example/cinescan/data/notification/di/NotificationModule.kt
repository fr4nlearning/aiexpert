package com.example.cinescan.data.notification.di

import com.example.cinescan.data.notification.WorkManagerNotificationScheduler
import com.example.cinescan.domain.notification.NotificationScheduler
import dagger.Binds
import dagger.Module
import dagger.hilt.InstallIn
import dagger.hilt.components.SingletonComponent
import javax.inject.Singleton

@Module
@InstallIn(SingletonComponent::class)
abstract class NotificationModule {

    @Binds
    @Singleton
    abstract fun bindNotificationScheduler(
        impl: WorkManagerNotificationScheduler
    ): NotificationScheduler
}
